    public static class Triangle {
        Color color;
        float [] v = new float [9];
    }

    private final Homogeneous.Point material = new Homogeneous.Point(0.5608f, 0.7373f, 0.5608f);

    // Just rotate by given sine and cosine.
    private void modelViewTriangle(float [] r, int ir, float [] p, int ip, float cos, float sin, Homogeneous.Point pos) {
        for (int i = 0; i < 3; i++, ir += 3, ip += 3) {
            r[ir + 0] = cos * p[ip + 0] - sin * p[ip + 2] + pos.x();
            r[ir + 1] = p[ip + 1] + pos.y();
            r[ir + 2] = cos * p[ip + 2] + sin * p[ip + 0] + pos.z();
        }
    }

    private final ArrayList<Triangle> triangles = new ArrayList<Triangle>(300);

    /**
     * Prepare internal data structures needed to render the tower with a given
     * transformation and at a given position and light vector.
     *
     */
    public Triangle [] getTriangles(float cos, float sin, Homogeneous.Point pos) {

        // Ensure sin and cos really are!
        float len = (float)Math.sqrt(cos * cos + sin * sin);
        cos /= len;
        sin /= len;
        
        // This is a huge hack to be able to use the SketchUp tower model without true HSR.
        //
        // For each triangle strip, transform the first triangle.  Compute z component of its normal by cross product.
        // If it's positive, the strip is visible.  Compute its correct lambertian setPaint.  Add first triangle to
        // list, then transform the rest and add them, all with computed setPaint.  When done, sort ascending on average z.
        // If we're lucky, that's good enough.
        for (int iStrip = 0; iStrip < strips.length; iStrip++) {
            TriangleStrip strip = strips[iStrip];
            Color color = null;
            for (int i9 = 0; i9 < strip.points.length; i9 += 9) {
                Triangle t = new Triangle();
                modelViewTriangle(t.v, 0, strip.points, i9, cos, sin, pos);
                if (i9 == 0) {
                    // Back face test done in world space may draw faces
                    // that are actually invisible, but they'll sort to rear
                    // in this picture.
                    final float ax = t.v[3 + 0] - t.v[0 + 0];
                    final float ay = t.v[3 + 1] - t.v[0 + 1];
                    final float bx = t.v[6 + 0] - t.v[0 + 0];
                    final float by = t.v[6 + 1] - t.v[0 + 1];
                    if (ax * by - bx * ay < 0) {
                        break;
                    }
                }
                if (color == null) {
                    // Find lambert setPaint for all triangles in this strip.
                    final float [] n = strip.normals;
                    final float nx = cos * n[0] - sin * n[2];
                    final float ny = n[1];
                    final float nz = cos * n[2] + sin * n[0];
                    final Homogeneous.Point l = FixedEyeTerrainModel.unitLight;
                    final float lamIntensity = Math.max(0f, l.x() * nx +  l.y() * ny + l.z() * nz);
                    final float tAmb = FixedEyeTerrainModel.ambientIntensity;
                    final float tLam = 1f - tAmb;
                    final float tMat = tAmb + tLam * lamIntensity;
                    int r = (int)(255.99f * (tMat * material.a[0]));
                    int g = (int)(255.99f * (tMat * material.a[1]));
                    int b = (int)(255.99f * (tMat * material.a[2]));
                    color = new Color(r, g, b);
                }
                t.color = color;
                triangles.add(t);
            }
        }
        // Very crude painters algorithm sort.  Works okay for this picture.
        Collections.sort(triangles, new Comparator<Triangle>() {
            private float zMin(Triangle t) {
                return Math.min(t.v[2], Math.min(t.v[5], t.v[8]));
            }
            public int compare(Triangle a, Triangle b) {
                float za = zMin(a);
                float zb = zMin(b);
                return za < zb ? -1 :
                       za > zb ? +1 : 0;
            }
        });
        final Triangle [] rtn = triangles.toArray(new Triangle[triangles.size()]);
        triangles.clear();
        return rtn;
    }

    private final Renderer3d renderer = new Renderer3d();

    public void paint(Graphics2D g, ViewportTransform viewportTransform, Triangle [] triangles) {
        renderer.begin(Renderer3d.TRIANGLES);
        for (int i = 0; i < triangles.length; i++) {
            Triangle t = triangles[i];
            renderer.setPaint(t.color);
            renderer.addVertex(g, viewportTransform, t.v, 0, 3);            
        }
        renderer.end(g);
    }
